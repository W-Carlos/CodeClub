/*

OPERADORES ARITMÉTICOS

+ Adição
- Subtração
* Muliplicação
/ Divisão
% Resto da divisão
++ Incremento
-- Drecremento
** Exponenciação

*/

/* ++ Incremento. Sempre irá adicionar 1 */
let Number = 10
Number++

console.log(Number)
// Resultado será 11

/*-- Drecremento. Sempre irá subtrair 1  */
let NumberOne = 10
NumberOne--

console.log(NumberOne)
// Resultado será 9

/* ** Exponenciação. Vai calcular numeros elevados */
let NumberTwo = 10

console.log(NumberTwo ** 2)
// Resultado será 100