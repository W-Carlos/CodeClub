/*

ESTRUTURA DE REPETIÇÃO - WHILE(ENQUNATO)

Obs: Devemos usar o while quando não é previsivel um evento acontecer. Quando não sabemos exatamente quando queremos que ele para.

*/

/* ============== EXEMPLO 1 ============== */
// Vai somar os numeros pulando de dois em dois, até chegar a 20.
let i = 0
while (i < 20) {
    console.log(i)
    i += 2
}

/* ============== EXEMPLO 2 ============== */
let i = 541356745645
