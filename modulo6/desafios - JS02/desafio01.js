const myNumber = 10

if(myNumber > 10) {
    console.log(`O número ${myNumber} é maior que 10!`)
} else if(myNumber < 10) {
    console.log(`O número ${myNumber} é menor que 10!`)
} else{
    console.log(`O número ${myNumber} é igual a 10!`)
}