function sum(number1, number2) {
    console.log(number1 + number2)
}

sum(2, 9) // Invoca a função junto com os argumentos.

function mult(number1, number2) {
    console.log(number1 * number2)
}

mult(50, 50)
mult(60, 2) // Podemos reutilizar as funções
